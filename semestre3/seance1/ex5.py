# Operateurs  arithmetiques
a = 2.
b = 3.
print("a + b=", a + b)
print("a - b=", a - b)
print("a * b=", a * b)
print("a / b=", a / b)
print("a ^ 2=", a ** 2)
print("a % b", a % b)

# <, >, <=, >=, ==, !=
a = 2  # Integer
b = 1.99  # Floating  point
print("a > b", a > b)
print("a < b", a < b)
print("a >= b", a >= b)
print("a <= b", a <= b)
print("a == b", a == b)
print("a != b", a != b)
